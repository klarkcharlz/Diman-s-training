"""
Теория множеств.
операции над множествами, теория множеств.
"""

a = {"a", "b", "c", "d"}
b = {"b", "d", "e", "f"}

print(f"a | b = {a | b}")  # объединение, всё что есть и в a и в b
print(f"a & b = {a & b}")  # пересечение, что есть общего у a и b
print(f"a - b = {a - b}")  # разность, что есть в a чего нет в b

